<?php

declare(strict_types=1);

namespace LlmDispatch\Runner\Tests\Unit\Evaluator\Check;

use LlmDispatch\Runner\Dispatch\ClaudeCli;
use LlmDispatch\Runner\Dispatch\ClaudeCliResponse;
use LlmDispatch\Runner\Dispatch\RateLimitInfo;
use LlmDispatch\Runner\Evaluator\Check\RubricScoreCheck;
use LlmDispatch\Runner\Judge\JudgeClient;
use PHPUnit\Framework\TestCase;

final class RubricScoreCheckTest extends TestCase
{
    public function testMedianOfThreeCallsPerCriterion(): void
    {
        $wt = $this->worktreeWithMemo("This memo lays out the plan.\nIt covers the backfill approach in detail.\n");
        $rubricPath = $this->rubricFile([
            ['id' => 'a', 'text' => 'Criterion A description', 'anchors' => [
                '0' => 'not covered', '1' => 'mentioned briefly', '2' => 'fully analyzed',
            ]],
            ['id' => 'b', 'text' => 'Criterion B description', 'anchors' => [
                '0' => 'not covered', '1' => 'mentioned briefly', '2' => 'fully analyzed',
            ]],
        ]);
        $cli = $this->fakeCli([
            '{"scores": {"a": 2, "b": 1}}',
            '{"scores": {"a": 0, "b": 1}}',
            '{"scores": {"a": 2, "b": 0}}',
        ]);
        $judge = new JudgeClient($cli, 'claude-opus-4-8', '/tmp');
        $check = new RubricScoreCheck($rubricPath, 'memo.md', 3, $judge);

        $result = $check->run($wt);

        $this->assertSame(['a' => 2, 'b' => 1], $result->details['metrics']['rubric_per_criterion']);
        $this->assertSame(3, $result->details['metrics']['rubric_total']);
        $this->assertTrue($result->passed);
        $this->assertSame(3, $cli->calls);
    }

    public function testModelNameLinesAreStrippedFromMemo(): void
    {
        $memo = "This memo lays out the plan.\nGenerated by Claude Opus\nIt covers the backfill approach.\n";
        $wt = $this->worktreeWithMemo($memo);
        $rubricPath = $this->rubricFile([
            ['id' => 'a', 'text' => 'Identifies the plan', 'anchors' => [
                '0' => 'not covered', '1' => 'noted', '2' => 'fully analyzed',
            ]],
        ]);
        $cli = $this->fakeCli([
            '{"scores": {"a": 1}}',
            '{"scores": {"a": 1}}',
            '{"scores": {"a": 1}}',
        ]);
        $judge = new JudgeClient($cli, 'claude-opus-4-8', '/tmp');
        $check = new RubricScoreCheck($rubricPath, 'memo.md', 1, $judge);

        $result = $check->run($wt);

        $this->assertNotEmpty($cli->prompts);
        $this->assertStringNotContainsString('Opus', $cli->prompts[0]);
        $this->assertSame(1, $result->details['blinded_lines']);
    }

    public function testMissingMemoFails(): void
    {
        $wt = $this->emptyWorktree();
        $rubricPath = $this->rubricFile([
            ['id' => 'a', 'text' => 'x', 'anchors' => ['0' => 'a', '1' => 'b', '2' => 'c']],
        ]);
        $cli = $this->fakeCli([]);
        $judge = new JudgeClient($cli, 'claude-opus-4-8', '/tmp');
        $check = new RubricScoreCheck($rubricPath, 'memo.md', 1, $judge);

        $result = $check->run($wt);

        $this->assertFalse($result->passed);
        $this->assertTrue($result->details['artifact_missing']);
        $this->assertNull($result->details['metrics']);
        $this->assertSame(0, $cli->calls);
    }

    public function testMissingRubricFileFailsWithoutJudgeCalls(): void
    {
        $wt = $this->worktreeWithMemo("This memo is valid.\nIt has content.\n");
        $rubricPath = '/nonexistent/rubric_' . bin2hex(random_bytes(6)) . '.json';
        $cli = $this->fakeCli([]);
        $judge = new JudgeClient($cli, 'claude-opus-4-8', '/tmp');
        $check = new RubricScoreCheck($rubricPath, 'memo.md', 1, $judge);

        $result = $check->run($wt);

        $this->assertFalse($result->passed);
        $this->assertSame('rubric missing or invalid', $result->details['error']);
        $this->assertNull($result->details['metrics']);
        $this->assertSame(0, $cli->calls);
    }

    public function testNumericStringVotesAreParsed(): void
    {
        $wt = $this->worktreeWithMemo("This memo demonstrates the approach.\n");
        $rubricPath = $this->rubricFile([
            ['id' => 'a', 'text' => 'Criterion A', 'anchors' => [
                '0' => 'not covered', '1' => 'mentioned', '2' => 'fully analyzed',
            ]],
        ]);
        $cli = $this->fakeCli([
            '{"scores": {"a": "2"}}',
            '{"scores": {"a": 2}}',
            '{"scores": {"a": "1"}}',
        ]);
        $judge = new JudgeClient($cli, 'claude-opus-4-8', '/tmp');
        $check = new RubricScoreCheck($rubricPath, 'memo.md', 1, $judge);

        $result = $check->run($wt);

        $this->assertSame(['a' => 2], $result->details['metrics']['rubric_per_criterion']);
        $this->assertSame(2, $result->details['metrics']['rubric_total']);
        $this->assertTrue($result->passed);
    }

    public function testConstructorRejectsZeroJudgeCalls(): void
    {
        $this->expectException(\InvalidArgumentException::class);
        $cli = $this->fakeCli([]);
        $judge = new JudgeClient($cli, 'claude-opus-4-8', '/tmp');
        new RubricScoreCheck('/tmp/rubric.json', 'memo.md', 1, $judge, 0);
    }

    private function worktreeWithMemo(string $memo): string
    {
        $dir = $this->emptyWorktree();
        mkdir($dir . '/mock-project');
        file_put_contents($dir . '/mock-project/memo.md', $memo);
        return $dir;
    }

    private function emptyWorktree(): string
    {
        $dir = sys_get_temp_dir() . '/rsc_' . bin2hex(random_bytes(6));
        mkdir($dir, 0o777, true);
        return $dir;
    }

    /**
     * @param list<array<string, mixed>> $criteria
     */
    private function rubricFile(array $criteria): string
    {
        $path = sys_get_temp_dir() . '/rubric_' . bin2hex(random_bytes(6)) . '.json';
        file_put_contents($path, (string) json_encode(['criteria' => $criteria]));
        return $path;
    }

    /** @param list<string> $texts */
    private function fakeCli(array $texts): ClaudeCli
    {
        return new class($texts) implements ClaudeCli {
            public int $calls = 0;
            /** @var list<string> */
            public array $prompts = [];

            /** @param list<string> $texts */
            public function __construct(private array $texts) {}

            public function dispatch(string $prompt, string $modelId, string $cwd, array $allowedTools): ClaudeCliResponse
            {
                $this->prompts[] = $prompt;
                $text = $this->texts[$this->calls] ?? '';
                $this->calls++;
                return new ClaudeCliResponse(
                    isError: false, resultText: $text, modelIdReported: $modelId,
                    inputTokens: 1, outputTokens: 1, durationMs: 1, stopReason: 'end_turn',
                    costUsd: 0.0, rateLimit: new RateLimitInfo('ok', null),
                    rawStdout: '', rawStderr: '', exitCode: 0,
                );
            }
        };
    }
}
